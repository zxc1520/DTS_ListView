package id.afif.dts_listview.collections;

public class Negara {

    public String nama;
    public String detail;

    public void setNama(String nama) {
        this.nama = nama;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getNama() {
        return nama;
    }

    public String getDetail() {
        return detail;
    }

}
