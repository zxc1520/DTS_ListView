package id.afif.dts_listview;

public class Negara {

    public String nama;
    public String detail;

    public Negara(String nama, String detail) {
        this.nama = nama;
        this.detail = detail;
    }

    public String getNama() {
        return nama;
    }

    public String getDetail() {
        return detail;
    }

}
